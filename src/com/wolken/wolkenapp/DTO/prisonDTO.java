package com.wolken.wolkenapp.DTO;

public class prisonDTO {
 private int prisonid;
 private String prison_name;
 private int no_of_inmates;
 private int no_of_years;
 
 
public int getPrisonid() {
	return prisonid;
}
public void setPrisonid(int prisonid) {
	this.prisonid = prisonid;
}
public String getPrison_name() {
	return prison_name;
}
public void setPrison_name(String prison_name) {
	this.prison_name = prison_name;
}
public int getNo_of_inmates() {
	return no_of_inmates;
}
public void setNo_of_inmates(int no_of_inmates) {
	this.no_of_inmates = no_of_inmates;
}
public int getNo_of_years() {
	return no_of_years;
}
public void setNo_of_years(int no_of_years) {
	this.no_of_years = no_of_years;
}
 
 
 
 
 
}
